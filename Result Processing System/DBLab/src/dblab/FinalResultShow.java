    private void idBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idBoxActionPerformed
        // TODO add your handling code here:
        /*int count = searchBox.getSelectedIndex();
        def.finalResultShowStuSearch(count, searchField.getText(), this);*/
        if (check == 1) {
            if (searchField.getText() == null) {
                searchField.setText("");
            }
            String column = "";
            if (searchBox.getSelectedIndex() == 0) {
                column = "r.id";
            } else if (searchBox.getSelectedIndex() == 1) {
                column = "student_name";
            }

            def.finalResultRefreshTable(this);

            if (orderBox.getSelectedIndex() == 0) {
                def.finalResultByAsc(this, def.getBatch(), "" + def.getSemester(), idBox.getSelectedIndex(), column, searchField.getText());
            } else if (orderBox.getSelectedIndex() == 1) {
                def.finalResultByDesc(this, def.getBatch(), "" + def.getSemester(), idBox.getSelectedIndex(), column, searchField.getText());
            }
        } else {
            JOptionPane.showMessageDialog(null, "First save the result to Database");
        }
    }//GEN-LAST:event_idBoxActionPerformed

