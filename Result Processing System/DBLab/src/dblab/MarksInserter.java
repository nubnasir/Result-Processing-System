    private void preButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_preButtonActionPerformed
        // TODO add your handling code here:
        c_count = def.course_counter;
        c_count--;
        def.course_counter--;
        if (c_count <= 0) {
            c_count = 0;
            def.course_counter = 0;
        }
        if ("NORMAL".equals(def.marking_type[c_count])) {
            markPanel.setVisible(true);
            labPanel.setVisible(false);
            vivaPanel.setVisible(false);
            def.preMark(this, c_count);
            preCounter++;
        } else if ("LAB".equals(def.marking_type[c_count])) {
            markPanel.setVisible(false);
            labPanel.setVisible(true);
            vivaPanel.setVisible(false);
            def.preLab(this, c_count);
            preCounter++;
        } else if ("VIVA".equals(def.marking_type[c_count])) {
            markPanel.setVisible(false);
            labPanel.setVisible(false);
            vivaPanel.setVisible(true);
            def.preViva(this, c_count);
            preCounter++;
        }
        subjectField.setText(def.course_title[def.course_counter]);
        subjectCodeField.setText(def.course_code[def.course_counter] + " and " + def.cradit[def.course_counter]);
    }//GEN-LAST:event_preButtonActionPerformed

