    private void nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextActionPerformed
        // TODO add your handling code here:

        if ("NORMAL".equals(def.marking_type[def.course_counter])) {
            def.checkerOfNullInputMark(this);
        } else if ("LAB".equals(def.marking_type[def.course_counter])) {
            def.checkerOfNullInputLab(this);
        } else if ("VIVA".equals(def.marking_type[def.course_counter])) {
            def.checkerOfNullInputViva(this);
        }


        if (getCheckNull() == 1) {

            if (def.course_counter == def.number_of_courses - 2) {
                def.nextCourseMarks(this);
            } else if (def.course_counter < def.number_of_courses - 1) {
                def.nextCourseMarks(this);
            } else {
                def.saveCourseMarks(this);
                def.resultCalculation();
                FinalResultShow frs = new FinalResultShow(def);
                def.showFinalResult(frs);
                def.course_counter--;
                JOptionPane.showMessageDialog(null, "All marks are taken.");
                frs.setVisible(true);
                setVisible(false);
                ncat.stop();
                lcat.stop();
                nft.stop();
                lft.stop();
                vt.stop();
            }

            //System.out.println("--------------------------------------------------c_c " + def.course_counter + " S_c " + def.see_course_counter);

            if (def.course_counter < preCounter) {
                if ("NORMAL".equals(def.marking_type[def.course_counter])) {
                    markPanel.setVisible(true);
                    labPanel.setVisible(false);
                    vivaPanel.setVisible(false);
                    def.preMark(this, def.course_counter);
                    preCounter--;
                    if (preCounter <= 0) {
                        preCounter = 0;
                    }
                } else if ("LAB".equals(def.marking_type[def.course_counter])) {
                    markPanel.setVisible(false);
                    labPanel.setVisible(true);
                    vivaPanel.setVisible(false);
                    def.preLab(this, def.course_counter);
                    preCounter--;
                    if (preCounter <= 0) {
                        preCounter = 0;
                    }
                } else if ("VIVA".equals(def.marking_type[def.course_counter])) {
                    markPanel.setVisible(false);
                    labPanel.setVisible(false);
                    vivaPanel.setVisible(true);
                    def.preViva(this, def.course_counter);
                    preCounter--;
                    if (preCounter <= 0) {
                        preCounter = 0;
                    }
                }
            }

            setCheckNull(0);
        } else {
            JOptionPane.showMessageDialog(null, "Correctly insert your information");
        }
    }//GEN-LAST:event_nextActionPerformed

