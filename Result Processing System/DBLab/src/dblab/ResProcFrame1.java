    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int value;
        try {
            value = Integer.parseInt(sem.getText());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Semester must be a number.");
            return;
        }
        if (num == 2) {
            def.checkExistenceOfaSemesterResult(batchNumber.getText(), value, this);
            if (getChecker() == 1) {
                try {
                    MarksInserter mark = new MarksInserter(def);
                    int a = Integer.parseInt(sem.getText());
                    def.processResult1(batchNumber.getText(), a, mark);
                    def.setBatch(batchNumber.getText());
                    def.setSemester(a);
                    mark.setVisible(true);
                    setVisible(false);

                    st.setVisible(false);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Semester must be a number.");
                }
            } else {
                int a = def.checkTEMarksInBatchSemester();
                if (a == 0) {
                    JOptionPane.showMessageDialog(null, "Batch: " + batchNumber.getText() + " and Semester: " + sem.getText() + " Result already exits.\nThank you");
                } else {
                    if (JOptionPane.showConfirmDialog(null, "Batch: " + batchNumber.getText() + " and Semester: " + sem.getText() + " Result already exits.\nInsert thired examiners marks?") == 0) {
                        InsertTEMarks te = new InsertTEMarks(def, batchNumber.getText(), sem.getText());
                        te.infoLebel.setText("3rd Examinee Marks for Batch : " + batchNumber.getText() + ", Semester : " + sem.getText());
                        def.showTEStuAndSub(batchNumber.getText(), sem.getText(), te);

                        te.setVisible(true);
                        st.setVisible(false);
                    }
                }

            }
        } else if (num == 1) {
            int a = def.checkTEMarksInBatchSemester();
            if (a == 0) {
                JOptionPane.showMessageDialog(null, "Batch: " + batchNumber.getText() + " and Semester: " + sem.getText() + " Result does not need third examinee marks.\nThank you");
            } else {
                InsertTEMarks te = new InsertTEMarks(def, batchNumber.getText(), sem.getText());
                te.infoLebel.setText("3rd Examinee Marks for Batch : " + batchNumber.getText() + ", Semester : " + sem.getText());
                def.showTEStuAndSub(batchNumber.getText(), sem.getText(), te);

                te.setVisible(true);
            }
        }
}//GEN-LAST:event_jButton1ActionPerformed
