    private void idBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_idBoxActionPerformed
        // TODO add your handling code here:

        if (idField.getText() == null) {
            idField.setText("");
        }
        if (nameField.getText() == null) {
            nameField.setText("");
        }

        def.viewResultRefreshTable(this);

        if (orderBox.getSelectedIndex() == 0) {
            def.viewResultByASC(this, batch, semester, idField.getText(), nameField.getText(), idBox.getSelectedIndex());
        } else if (orderBox.getSelectedIndex() == 1) {
            def.viewResultByDESC(this, batch, semester, idField.getText(), nameField.getText(), idBox.getSelectedIndex());
        }
    }//GEN-LAST:event_idBoxActionPerformed

