    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // TODO add your handling code here:
        if (!normarUser) {
            if (JOptionPane.showConfirmDialog(null, "Are you sure? GO TO MAIN MENU =>") == 0) {
                this.setVisible(false);
                StartHere st = new StartHere();
                st.setVisible(true);
                st.MainPanel.setVisible(false);
                st.adminPanel.setVisible(true);
            }
        } else {
            this.setVisible(false);
            StartHere st = new StartHere();
            st.setVisible(true);
            st.MainPanel.setVisible(true);
            st.adminPanel.setVisible(false);
            st.normalButton.setVisible(false);
            st.norViewInfo.setVisible(true);
            st.norBack.setVisible(true);
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed
    
